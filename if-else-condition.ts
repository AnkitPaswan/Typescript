// An if else condition includes two blocks - if block and an else block. 
// If the if condition evaluates to true, then the if block is executed. 
// Otherwies, the else block is executed.

// Syntax
// if(boolean_expression) {  
//    // statement(s) will execute if the boolean expression is true
// } else {
//    // statement(s) will execute if the boolean expression is false  
// }

//Example of if-else conditional
let x: number = 10, y = 20;

if (x > y) {
    console.log('x is greater than y.');
}
else {
    console.log('x is less than or equal to y.'); //This will be executed
}

var num = 12;
if (num % 2 == 0) {
    console.log("Even");
} else {
    console.log("Odd");
}


