// for...of Loop
// TypeScript includes the for...of loop to iterate and access elements of an array, list, 
// or tuple collection. The for...of loop returns elements from a collection e.g. array,
//  list or tuple, and so, there is no need to use the traditional for loop shown above.


let arr = [10, 20, 30, 40];

for (var val of arr) {
    console.log(val); // prints values: 10, 20, 30, 40
}